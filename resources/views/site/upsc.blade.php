    <h3>Assistant Architect Recruitment through UPSC</h3>
    <p>UPSC is recruiting Assistant Architect by means of (Vacancy No.20070711525) One
    vacancy for the post of Architect (Group-A), Urban Planning, Department of Urban
    Planning (Architect Wing), Chandigarh Administration (UR-01). The post is suitable
    for Physically Challenged person viz. Deaf and Hard of Hearing with disability i.e.
    Hard of Hearing (HH). The post is temporary but likely to be continued indefinitely.
    Pay Scale: Rs.15600-39100 + Grade Pay Rs.7600. (Pre revised). General Central
    Service, Group ‘A’ Gazetted, Non-Ministerial. Age: 40 years. ESSENTIAL
    QUALIFICATIONS: (A) EDUCATIONAL:(i)Degree in Architecture of a recognized
    University or equivalent. (ii) Should be registered as Architect with Council of
    Architecture, India. (B) EXPERIENCE:Five years experience in the Profession.
    NOTE-: Qualifications are relaxable at the discretion of the Union Public Service
    Commission in the case of the candidates otherwise well qualified. DUTIES:The
    Architect collects information regarding building from client department. He meets
    the client department and Engineers and finalizes sketch design. He asks for
    structural, public health, sanitary and electrical proposals from respective Engineers
    for designs and after administrative approval prepares working drawings of the
    buildings. HQ: Chandigarh.</p>

